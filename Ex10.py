first_number = float(input("Enter the first number"))
second_number = float(input("Enter the second number"))
operator = input("operator")

if operator == '+':
    print("result :" , first_number + second_number)
elif operator == '-':
    print("result :" , first_number - second_number)

elif operator == '*':
    print("result :" , first_number * second_number)

elif operator == '/':
    print("result :" , first_number / second_number)
    